package com.starktech.motherhood;

public class Members {

    private String name;
    private String isMom;
    private String isPreg;
    private String cityLoc;
    private String userid;


    public Members() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIsMom() {
        return isMom;
    }

    public void setIsMom(String isMom) {
        this.isMom = isMom;
    }

    public String getIsPreg() {
        return isPreg;
    }

    public void setIsPreg(String isPreg) {
        this.isPreg = isPreg;
    }

    public String getCityLoc() {
        return cityLoc;
    }

    public void setCityLoc(String cityLoc) {
        this.cityLoc = cityLoc;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }


}
